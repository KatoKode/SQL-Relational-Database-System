  std::pair<red::buffer, red::buffer> sql::compile_stmt () {
    try {
      tran::lexer tran_lexer {m_stmt};
      tran::parser tran_parser {tran_lexer};
      tran_parser.parse();
      comp::comp_lexer comp_lexer {tran_parser.dump()};
      comp::comp_parser comp_parser {comp_lexer};
      comp_parser.parse();
#ifdef SQL_DEBUG
      comp_parser.dump2();
#endif
      return comp_parser.dump();
    } catch (...) { throw; }
  }

